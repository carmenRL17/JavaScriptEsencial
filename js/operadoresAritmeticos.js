// los operadores aritmeticos.
var datoA = 10;
var datoB = 20;

//SUMA +
const suma = datoA + datoB;
console.log(suma);

//RESTA -
const resta = datoA - datoB;
console.log(resta);

//MULTIPLICACION *
const multiplicacion = datoA * datoB;
console.log(multiplicacion);
//DIVISION /
const division = datoA / datoB;
console.log(division);
//MODULO O RESIDUO %
const residuo = datoA % datoB;
console.log(residuo);
//INCREMENTO ++
var incremento = datoA;
incremento ++;
console.log(incremento);
//DECREMENTO --
var decremento = datoA;
decremento--;
console.log(decremento);

